    private void modifyLastName() {
        String newLastName = usr_ln_mod.getText().trim();

        if (newLastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez entrer un nouveau nom.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (newLastName.split("\\s+").length > 2) {
            javax.swing.JOptionPane.showMessageDialog(this, "Le nom ne peut contenir que deux noms composés.", "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        String regex = "^[A-Za-z-]+$";

        String[] words = newLastName.split("\\s+");

        for (String word : words) {

            if (!word.matches(regex)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Le nom ne peut contenir que des lettres et des tirets.", "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        Users_mod modln = new Users_mod(
                Integer.parseInt(clientId),
                newLastName, null, null, null, null, null, null, null
        );

        try {
            // Mise à jour via le contrôleur
            controller.updateLastNameClients(modln);
            JOptionPane.showMessageDialog(this, "Nom mis à jour avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
            currentLastName = newLastName;
            jLabel3.setText("Nom Actuel : " + currentLastName);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la mise à jour du nom : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

